<p>A place where "Unity in diversity" gets true. 
Regional Engineering College, Calicut was established in 1961 and converted into an National Institute of Technology on June 26, 2002.  According to the National Institutional Ranking Framework (NIRF), the institute is ranked 35 in the 2016 engineering university rankings. The college started with an annual intake of 125 students for the undergraduate courses, on a campus of 120 hectares (1.2 km2). With an academic staff of 205,and student intake of undergraduate 4200 and postgraduates 1100 our prestigious college stands among the top colleges in the country.</p>
 
<p>The Institute offers undergraduate courses of four years duration in the disciplines of Civil Engineering, Chemical Engineering, Electrical and Electronics Engineering, Electronics and Communication Engineering, Mechanical Engineering, Production Engineering and Management, Computer Science and Engineering, Information Technology and a five-year B.Arch. course. The Institute also offers M.Tech. Degree in different specializations. In addition, three-year (six-semester) MCA programme is also offered. Four new PG courses have also been started in the areas of Civil, Information Security, VLSI Design and computer controlled Industrial power. The Institute offers Ph.D. programme in all its departments.</p>
